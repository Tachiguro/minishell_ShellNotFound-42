int	ft_isalnum(unsigned int i)
{
	if ((i <= 'Z' && i >= 'A') || (i <= 'z' && i >= 'a'))
		return (1);
	else if (i <= 57 && i >= 48)
		return (1);
	else
		return (0);
}
